//Logical Operators
console.log("LOGICAL AND OPERATOR")
console.log(15 && 20)
console.log(true && 10)
console.log(false && 10)
console.log(true && false)
console.log(false && true)

console.log("LOGICAL OR OPERATOR")
console.log(5 || 10)
console.log(true || 10)
console.log(false || 10)
console.log(true || false)
console.log(false || true)

